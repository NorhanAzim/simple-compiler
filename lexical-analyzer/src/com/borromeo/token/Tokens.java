/*
 * Class that defines a Token
 */

package com.borromeo.token;


public class Tokens {

	private String token;
	private String lexeme;
	
    public Tokens() {
    	token = "";
    	lexeme = "";
    }
    
    public Tokens(StringBuffer lex, String tok){
    	token = ""+tok;
    	lexeme = ""+lex;
    }
    
     public Tokens(char lex, String tok){
    	token = ""+tok;
    	lexeme = ""+lex;
    }
    
    public Tokens(String lex, String tok){
    	token = tok;
    	lexeme = lex;
    }
    
    public String getToken(){
    	return token;
    }
    
    public String getLexeme(){
    	return lexeme;
    }
    
    public String toString(){
    	return (token + "\t\t" + lexeme);
    }
    
}